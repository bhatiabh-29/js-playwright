//variables are foundation of programming, they are used to store data and values that can be used and manipulated throughout the code. In JavaScript, variables can be declared using the var, let, or const keywords.
//let can be reassigned, while const cannot be reassigned after it has been initialized. Variables can hold different types of data, such as numbers, strings, booleans, arrays, and objects. They are essential for creating dynamic and interactive web applications. Proper naming conventions and understanding variable scope are important for writing clean and efficient code.

//example of variable declaration and usage in JavaScript:
//using var
var name = "John";
console.log(name); // Output: John  
//using let
let age = 30;
console.log(age); // Output: 30
//using const
const pi = 3.14;
console.log(pi); // Output: 3.14            


//In modern JavaScript, we use let and const:

//let → use when the value changes
//const → use when the value never changes

const testerName = "BB";
let testCount= 0;

console.log(testerName); // Output: BB
console.log(testCount);

testCount= 5;
